

const dia = 0; // 0 : Domingo 


switch (dia){
    case 0:
        console.log("Domingo");
        break;
    case 1:
        console.log("Lunes");
        break;
    case 2:
        console.log("Martes");
        break;
    default:
        console.log("No es lunes , no es martes ni domingo");
}

// cada condicion case, necesita su break
// el default es como el else 


